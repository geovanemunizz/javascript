let number = 20;
number -= 10

let n1 = 30
let n2 = 15

let inv = -number //operador de inversão, se a variavel for positiva, vira negativa, se for negaviva fica positiva

console.log(number++); //pós incremento,é necessario chamar novamente a variavel para incrementar o valor em 1
console.log(number);

console.log(--number); //pré incremento, já executa o incremento em 1 e adiciona o valor a variavel

console.log(inv);

console.log(n1 + "" + n2); //concatenação

